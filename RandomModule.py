"""
This module is used to generate random numbers by the computer for you.
"""

import random

print(random.randrange(50)) # Will print a number which is never greater than 50
print(random.randrange(0,30)) # Will print a number just between 0 and 30
print(random.randrange(0,60,5)) # Will print a number just between 0 and 60 with the step size of 5